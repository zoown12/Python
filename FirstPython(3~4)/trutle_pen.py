import turtle

#--네모--
turtle.shape('turtle')
turtle.forward(200)
turtle.left(90)
turtle.forward(200)
turtle.left(90)
turtle.forward(200)
turtle.left(90)
turtle.forward(200)


#--색,사이즈--
turtle.pensize(5)
turtle.pencolor("red")
turtle.right(90)
turtle.forward(200)

#--펜 up,down--
turtle.penup()
turtle.right(90)
turtle.forward(200)
turtle.pendown()
turtle.right(90)
turtle.forward(200)
